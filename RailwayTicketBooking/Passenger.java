package RailwayTicketBooking;

public class Passenger {
    String name;
    int age;
    String gender;
    String child;
    String birthPreference;

    public Passenger(String name, int age, String gender,String child, String birthPreference) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.child = child;
        this.birthPreference = birthPreference;
    }

}
